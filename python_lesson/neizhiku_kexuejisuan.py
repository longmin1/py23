import math

print(math.pi)
print(math.e)
print(math.inf)#正无穷大
print(-math.inf)#负无穷大
print(math.nan)#不是数字

#向上取整  比4.3大的最小整数
print(math.ceil(4.3))#-》5
print(math.ceil(-4.3))#-》4
#向下取整  比4.3小的最大整数
print(math.floor(4.3))#》4
print(math.floor(-4.3))#》-5

#2的10次幂
print(math.pow(2, 10))#1024.0
#16开平方
print(math.sqrt(16))#4.0